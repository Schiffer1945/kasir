    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(Tanggal.getDate());
        String namakasir = tNamaKsr.getText();
        String makanan = (String) tMakanan.getSelectedItem();
        int hargamakanan = Integer.parseInt(tHargaMakanan.getText());
        String minuman = (String) tMinuman.getSelectedItem();
        int hargaminuman = Integer.parseInt(tHargaMinuman.getText());
        int totalharga = Integer.parseInt(tTotalHarga.getText());
        
        String SQL = "INSERT INTO tb_pesan (tanggal,kasir,makanan,hargamakanan,minuman,hargaminuman,totalHarga)" +
                "VALUES('"+tanggal+"','"+namakasir+"','"+makanan+"',"+hargamakanan+",'"+
                minuman+"',"+hargaminuman+","+totalharga+")";
        int status = KoneksiDB.execute(SQL);
        if(status == 1) {
            JOptionPane.showMessageDialog(this, "Data berhasil ditambah", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal ditambah", "GAGAL", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bSaveActionPerformed

    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        int baris = tabelData.getSelectedRow();
        if(baris != -1) {
            String id = tabelData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_pesan WHERE id='"+id+"'";
            int status = KoneksiDB.execute(SQL);
            if(status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris data dulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bDeleteActionPerformed

