    private void selectData() {
        String kolom[] = {"id", "Tanggal", "kasir", "makanan", "hargamakanan", "minuman", "hargaminuman", "totalHarga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_pesan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
               String id = rs.getString(1);
               String Tanggal = rs.getString(2);
               String kasir = rs.getString(3);
               String makanan = rs.getString(4);
               String hargamakanan = rs.getString(5);
               String minuman = rs.getString(6);
               String hargaminuman = rs.getString(7);
               String totalHarga = rs.getString(8);
               
               String data[] = {id, Tanggal, kasir, makanan, hargamakanan, minuman, hargaminuman, totalHarga};
               dtm.addRow(data);
            }
        } catch(SQLException ex) {
            Logger.getLogger(mainFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabelData.setModel(dtm);
                
    }
